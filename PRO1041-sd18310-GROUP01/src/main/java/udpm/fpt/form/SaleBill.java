    private void btnupdatehdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdatehdActionPerformed
        if (this.checkifo()) {
            Sale_Bill1 sb = this.getSale_Bill1();
            int id = Integer.valueOf(txtidhd.getText());
            try {
                if (sgb.update(id, sb)) {
                    JOptionPane.showMessageDialog(this, "Sửa thành công");
                    LoadData();
                } else {
                    JOptionPane.showMessageDialog(this, "Sửa thành thất bại");
                    return;
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnupdatehdActionPerformed

