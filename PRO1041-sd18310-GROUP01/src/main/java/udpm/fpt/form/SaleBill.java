    public void LoadData() {
        dtm.setRowCount(0);
        for (Sale_Bill1 sb : this.sgb.getAll()) {
            Object[] ob = {
                sb.getID(),
                sb.getSE(),
                sb.getDC(),
                sb.getPD(),
                sb.getSD(),
                sb.getED(),
                sb.getSFID(),
                sb.getCRAT()
            };
            dtm.addRow(ob);
        }
    }

    public Sale_Bill1 getSale_Bill1() {
        Sale_Bill1 sb = new Sale_Bill1();
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd", Locale.ENGLISH);

        sb.setID(Integer.parseInt(txtidhd.getText()));
        sb.setSE(txtsehd.getText());
        sb.setDC(txtdkgghd.getText());
        sb.setPD(Double.parseDouble(txtptg.getText()));
        try {
            sb.setSD(formatter.parse(txtdshd.getText()));
        } catch (Exception e) {
            System.out.println("Err" + e);

        }
        try {
            sb.setED(formatter.parse(txtdehd.getText()));
        } catch (Exception e) {
            System.err.println("Err" + e);
        }
        sb.setSFID(Integer.parseInt(txtidnvhd.getText()));
//        try{
//            sb.setCRAT(formatter.parse(txtnt.getText()));
//        }catch(Exception e){
//            System.err.println("Err" + e);
//        }
        return sb;
    }

    public boolean checkifo() {
        if (txtidhd.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập thông tin");
            return false;
        } else if (txtsehd.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập thông tin");
            return false;
        } else if (txtdkgghd.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập thông tin");
            return false;
        } else if (txtptg.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập thông tin");
            return false;
        } else if (Double.valueOf(txtptg.getText()) > 100) {
            JOptionPane.showMessageDialog(this, "Vui lòng ko nhập quá 100");
            return false;
        } else if (txtdshd.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập thông tin");
            return false;
        } else if (txtdehd.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập thông tin");
            return false;
        } else if (txtidnvhd.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập thông tin");
            return false;
        }
//       else if(txtnt.getText().isBlank()){
//           JOptionPane.showMessageDialog(this, "Vui lòng nhập thông tin");
//           return false;
//       }

        return true;

    }

    public void timKiemSE(String SE) {
        String timTen = txtsearchhd.getText();
        if (timTen == null) {
            SE = "";
        }
        dtm = (DefaultTableModel) tblghh.getModel();
        dtm.setRowCount(0);
        for (Sale_Bill1 sb : sgb.timkiem(SE)) {
            Object[] ob = new Object[]{
                sb.getID(),
                sb.getSE(),
                sb.getDC(),
                sb.getPD(),
                sb.getSD(),
                sb.getED(),
                sb.getSFID(),
                sb.getCRAT()
            };
            dtm.addRow(ob);
        }
    }
