    private void initTableData() {
        EventAction eventAction = new EventAction() {
            @Override
            public void delete(ModelStudent student) {
                JOptionPane.showMessageDialog(null, table1.getValueAt(table1.getSelectedRow(), 1));
            }

            @Override
            public void update(ModelStudent student) {
                JOptionPane.showMessageDialog(null, "Update success");
            }
        };
        try {
            this.spring.rs = spring.GET("SELECT * FROM STUDENTS");
            while (spring.rs.next()) {
                table1.addRow(new ModelStudent(new ImageIcon(getClass().getResource(spring.rs.getString("IC"))), spring.rs.getString("NAME"), spring.rs.getString("GENDER"), spring.rs.getString("COURSE"), 200).toRowTable(eventAction));
            }

        } catch (SQLException e) {
            e.printStackTrace(System.out);
        }
    }

